
public class Ranking {
	
	private String nomes;
	private int pontos;
	
	public Ranking() {
		
	}
	
	public void setNomes(String nomes) {
		this.nomes = nomes;
	}
	
	public String getNomes() {
		return nomes;
	}
	
	public void setPontos(int pontos) {
		this.pontos = pontos;
	}
	
	public int getPontos() {
		return pontos;
	}
	

}
